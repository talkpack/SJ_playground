package model.statistics;

public class Keyword {
private String city;
private int count;
private int total;
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}
public int getTotal() {
	return total;
}
public void setTotal(int total) {
	this.total = total;
}

}
